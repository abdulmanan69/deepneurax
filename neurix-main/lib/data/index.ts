// Helper function to get service by slug
export function getServiceBySlug(slug: string) {
  return servicesData.find(s => s.slug === slug || String(s.id) === slug) || null
}

// Helper function to get product by slug
export function getProductBySlug(slug: string) {
  return productsData.find(p => p.slug === slug || String(p.id) === slug) || null
}

// Helper function to get case study by slug
export function getCaseStudyBySlug(slug: string) {
  return caseStudiesData.find(c => c.slug === slug || String(c.id) === slug) || null
}

// Helper function to get blog post by slug
export function getBlogPostBySlug(slug: string) {
  return blogPostsData.find(b => b.slug === slug || String(b.id) === slug) || null
}

